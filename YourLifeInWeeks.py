#Life in Weeks#
#1 year 365 days
#1 year 52 week
#1 year 12 months

days=int(365)
weeks=int(52)
months=int(12)

age =int(input("What is your current age? \n"))
      
days = int(age)*int(days)

weeks = int(age)*int(weeks)

months = int(age)*int(months)

print(f"You have {days} days,{weeks} weeks and {months}months left")